public class IntegerSample {

    public static void main(String[] args) {
        int age = 10; // 10
        age = age + 40; // 50
        age += 40; // 90
        age += 1; // 91
        age++; // 92 (increment)
        ++age; // 93

        System.out.println(age);
    }
}
